package com.frain.entity;

public class Books {
	String name;
	String Id;
	String major;
	boolean isLoan;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public boolean isLoan() {
		return isLoan;
	}
	public void setLoan(boolean isLoan) {
		this.isLoan = isLoan;
	}
	
	
	
	
}
